// String
let name = 'John Doe'
console.log(typeof name); 

// Number
name = 45;
console.log(typeof name);

// Boolean
name = true;
console.log(typeof name);

// Null
name = null;
console.log(typeof name);

// Undefined
let name1;
console.log(typeof name1);

// Array
name = ['ram','shyam'];
console.log(typeof name);

// Object Literals
name = {
    city: 'Boston',
    state: 'MA'
}
console.log(typeof name);

// Date
let today = new Date();
console.log(today);
console.log(typeof today);

let sym = Symbol() // Assignment
